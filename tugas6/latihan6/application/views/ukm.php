	<h2>	
	Visi dan Misi Paduan Suara

	Visi : 
	1. Menjadi wadah untuk menampung, mengembangkan dan mengekspresikan seni bernyanyi dalam paduan suara.
	2. Menjadi paduan suara mahasiswa yang berkualitas.

	Misi : 
	1. Mengadakan pentas mini dalam kampus maupun konser-konser yang mengangkat tema-tema tertentu yang melibatkan pihak internal dan eksternal kampus. 
	2. Melakukan proses latihan yang teratur dan intensif dengan menggunakan metode dan materi yang berbobot.


	Kegiatan-kegiatan Paduan Suara yang pernah dilakukan yaitu:
	1. Lomba Kesenian
	2. Dies Natalis 2016
	3. Wisuda 2016
